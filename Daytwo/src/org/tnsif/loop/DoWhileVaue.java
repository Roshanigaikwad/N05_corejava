package org.tnsif.loop;

import java.util.Scanner;

public class DoWhileVaue {

	public static void main(String[] args) {
		int count=0;
		{
			System.out.println("count of number:");
			
		}
		int num=34567;
		do
		{
			num=num/10;
			count++;
			
		}while(num!=0);
		{
			System.out.println(+count);
		}
			
	}
}
		
		
	

	

	   

